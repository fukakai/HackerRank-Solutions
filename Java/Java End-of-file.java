import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        int line = 1;
        Scanner scanner = new Scanner(System.in);
        while(scanner.hasNext()){
            System.out.println(line+" "+scanner.nextLine());
            line++;
        }
    }
}